CREATE TABLE posts (
	id                 integer PRIMARY KEY,
	site_name          varchar(100),
	city               varchar(100),
	location           varchar(100),
	post_title         varchar(100),
	post_age           varchar(100),
	post_text          varchar(10000),
	post_creation_date varchar(100),
	post_url           varchar(500),
	post_author        varchar(100),
	post_email         varchar(100),
	post_phone         varchar(100),
	post_price         varchar(100),
	post_id            varchar(100)
)
